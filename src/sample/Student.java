package sample;

public class Student {
    private String Name;
    private String RegNo;
    private String Book;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getRegNo() {
        return RegNo;
    }

    public void setRegNo(String regNo) {
        RegNo = regNo;
    }

    public String getBook() {
        return Book;
    }

    public void setBook(String book) {
        Book = book;
    }
}
